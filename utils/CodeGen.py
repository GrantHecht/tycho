"""TychoHeaderGen — SymPy-based C++ VectorFunction code generator.

Generates CSE-optimized VectorFunction structs with analytic Jacobian,
adjoint gradient, and adjoint Hessian from SymPy expression matrices.

Usage:
    from CodeGen import TychoHeaderGen

    header = TychoHeaderGen("MyFunc", Eq, sp.Matrix(Xs),
                            [(mu, "gravitational parameter")])
    header.make_header(output_dir="include/tycho/detail/astro")
"""

import os
import re

import sympy as sp

# Indent unit used in generated code
_IND = "    "

# Supported pow exponent ranges. Shared between powsimp (the regex-based
# post-CSE rewriter) and _extract_compound_pow_bases (the pre-CSE walker
# that lifts compound bases out of Pow). Keeping both in sync prevents a
# compound base from being lifted into an _hpN_ temporary that powsimp
# then fails to rewrite, leaving a literal pow() call in generated code.
_POW_INT_RANGE = range(2, 17)  # pow(x, 2) .. pow(x, 16)
_POW_NEG_INT_RANGE = range(1, 17)  # pow(x, -1) .. pow(x, -16)
_POW_HALF_INT_RANGE = range(0, 7)  # pow(x, ±0.5) .. pow(x, ±6.5)

# Reserved symbol pattern for precompute-discovered members.  Inputs and
# parameters that match this name pattern would be silently mis-classified
# by _partition_cses (which treats pcN_ symbols as parameters so the
# post-diff pass doesn't double-count them).
_PC_RESERVED = re.compile(r"^pc\d+_$")


def _format_default_literal(value):
    """Format a Python scalar as a C++ literal for the delegating default ctor.

    Handles int → "<n>" and float → "<x>.<y>" with a trailing decimal so
    that 1 emits as "1.0" (avoiding integer-narrowing warnings in the
    generated ctor delegation).
    """
    if isinstance(value, bool):
        # bool is a subclass of int — guard against accidental True/False.
        raise TypeError(
            f"Default value for scalar param must be int or float, got bool {value!r}"
        )
    if isinstance(value, int):
        return f"{value}.0"
    if isinstance(value, float):
        s = repr(value)
        # repr returns "1.0" for floats already; integers cast to float
        # similarly. No special handling needed beyond ensuring a decimal.
        return s if "." in s or "e" in s or "E" in s else f"{s}.0"
    raise TypeError(
        f"Default value for scalar param must be int or float, got {type(value).__name__} {value!r}"
    )


