"""TychoHeaderGen — SymPy-based C++ VectorFunction code generator.

Generates CSE-optimized VectorFunction structs with analytic Jacobian,
adjoint gradient, and adjoint Hessian from SymPy expression matrices.

Usage:
    from CodeGen import TychoHeaderGen

    header = TychoHeaderGen("MyFunc", Eq, sp.Matrix(Xs),
                            [(mu, "gravitational parameter")])
    header.make_header(output_dir="include/tycho/detail/astro")
"""

import os
import re

import sympy as sp

# Indent unit used in generated code
_IND = "    "

# Copyright header template
_COPYRIGHT = """\
// =============================================================================
// Tycho fork (Copyright 2026-present Grant R. Hecht, Apache 2.0 — see LICENSE.txt)
//
// Generated by {script} — do not edit manually.
// =============================================================================
"""

# Using declarations injected into the namespace block
_USING_DECLS = """\
// Import cross-namespace types from vf.
using vf::CMatRef;
using vf::CVecRef;
using vf::DenseDerivativeMode;
using vf::MatRef;
using vf::VecRef;
using vf::VectorFunction;
"""


class TychoHeaderGen:
    def __init__(
        self,
        Name,
        F,
        Xs,
        ScalarParams=[],  # [(Symbol, Description),]
        VectorParams=[],  # [(Vec, Cppname, Description),]
        MatrixParams=[],  # [(Mat, Cppname, Description),]
        docstr="A doc string",
        namespace="tycho::astro",
        include_path="tycho/vector_functions.h",
        gen_build_method=False,
    ):

        self.Name = Name
        self.namespace = namespace
        self.include_path = include_path
        self.gen_build_method = gen_build_method
        self.ninputs = len(Xs)
        self.noutputs = len(F)

        self.Func = sp.Matrix(list(F))
        self.Inputs = sp.Matrix(list(Xs))
        self.ScalarParams = ScalarParams
        self.VectorParams = VectorParams
        self.MatrixParams = MatrixParams

        self.docstr = docstr

        # Input symbol names (for dependency analysis)
        self._input_syms = {s for s in self.Inputs}

        print("Generating Jacobian")
        self.Jac = self.Func.jacobian(self.Inputs)
        print("Finished Jacobian")
        self.LMults = sp.Matrix(sp.symbols("LM:" + str(self.noutputs)))
        print("Generating Gradient")
        self.Grad = self.Jac.transpose() * self.LMults
        print("Finished Gradient")
        print("Generating Hessian")
        self.Hess = self.Grad.jacobian(self.Inputs)
        print("Finished Hessian")

        # ── Identify parameter-only subexpressions for precomputation ────
        # Run CSE on the full expression set to find subexpressions that
        # depend only on constructor parameters. These are stored for
        # the class header (member declarations + constructor init).
        # The _gen_cse_body() method handles per-method substitution.
        self._precomputed = []  # [(member_name, resolved_expr), ...]
        self._identify_precomputed()

    def _identify_precomputed(self):
        """Find param-only CSE subexpressions for constructor precomputation."""
        print("Identifying parameter-only subexpressions")
        all_cses, _ = sp.cse([self.Func, self.Jac, self.Grad, self.Hess])
        param_cses, _ = self._partition_cses(all_cses)

        if not param_cses:
            print("  No parameter-only subexpressions found")
            return

        print(f"  Found {len(param_cses)} parameter-only subexpressions")

        # Store with resolved member names. Each precomputed expr may
        # reference earlier precomputed values via their CSE symbols —
        # resolve those to member names (pcN_) for the constructor.
        member_syms = {}  # cse_sym → member Symbol
        for i, (cse_sym, cse_expr) in enumerate(param_cses):
            member_name = f"pc{i}_"
            member_sym = sp.Symbol(member_name)
            member_syms[cse_sym] = member_sym

            resolved_expr = cse_expr
            for prev_sym, prev_member in member_syms.items():
                if prev_sym == cse_sym:
                    break
                resolved_expr = resolved_expr.subs(prev_sym, prev_member)
            self._precomputed.append((member_name, resolved_expr))

    def _partition_cses(self, cses):
        """Split CSE list into (param_only, input_dependent).

        A CSE variable is parameter-only if its RHS expression contains
        no input symbols and no CSE variables that depend on inputs.
        """
        param_syms = set()
        for P, _ in self.ScalarParams:
            param_syms.add(P)
        for Vec, _, _ in self.VectorParams:
            for elem in Vec:
                param_syms.add(elem)
        for Mat, _, _ in self.MatrixParams:
            for elem in Mat:
                param_syms.add(elem)

        # Track which CSE symbols are parameter-only
        param_only_syms = set()
        param_cses = []
        input_cses = []

        for sym, expr in cses:
            free = expr.free_symbols
            if free <= (param_syms | param_only_syms):
                param_only_syms.add(sym)
                param_cses.append((sym, expr))
            else:
                input_cses.append((sym, expr))

        return param_cses, input_cses

    # ── pow simplification ──────────────────────────────────────────────

    def powsimp(self, expr):
        powcount = expr.count("pow")
        if powcount == 0:
            return expr

        # Convert integer pows to multiplies: pow(x, 2) → x*x
        for i in range(2, 16):
            regex = re.compile(rf"pow\((?P<var>[A-Za-z_]\w*)\s*,\s*{i}\s*\)")
            repl = r"\g<var>"
            for j in range(1, i):
                repl += r"*\g<var>"
            expr = re.sub(regex, repl, expr)

        # Convert pow(x, 0.5) → sqrt(x), pow(x, 1.5) → sqrt(x)*x, etc.
        for i in range(0, 7):
            regex = re.compile(rf"pow\((?P<var>[A-Za-z_]\w*)\s*,\s*{i}\.5\s*\)")
            repl = r"(sqrt(\g<var>)"
            for j in range(1, i + 1):
                repl += r"*\g<var>"
            repl += r")"
            expr = re.sub(regex, repl, expr)

        # Convert pow(x, -N) → Scalar(1.0)/(x*x*...)
        for i in range(1, 16):
            regex = re.compile(rf"pow\((?P<var>[A-Za-z_]\w*)\s*,\s*-{i}\s*\)")
            repl = r"(Scalar(1.0)/(\g<var>"
            for j in range(1, i):
                repl += r"*\g<var>"
            repl += r"))"
            expr = re.sub(regex, repl, expr)

        # Convert pow(x, -0.5) → Scalar(1.0)/sqrt(x)
        for i in range(0, 7):
            regex = re.compile(rf"pow\((?P<var>[A-Za-z_]\w*)\s*,\s*-{i}\.5\s*\)")
            repl = r"(Scalar(1.0)/(sqrt(\g<var>)"
            for j in range(1, i + 1):
                repl += r"*\g<var>"
            repl += r"))"
            expr = re.sub(regex, repl, expr)

        # Fractional forms: pow(x, 3.0/2.0) style
        for i in range(1, 10, 2):
            regex = re.compile(
                rf"pow\((?P<var>[A-Za-z_]\w*)\s*,\s*{i}\.0/2\.0\s*\)"
            )
            repl = r"(sqrt(\g<var>)"
            for j in range(0, int((i - 1) / 2)):
                repl += r"*\g<var>"
            repl += r")"
            expr = re.sub(regex, repl, expr)

        for i in range(1, 10, 2):
            regex = re.compile(
                rf"pow\((?P<var>[A-Za-z_]\w*)\s*,\s*-{i}\.0/2\.0\s*\)"
            )
            repl = r"(Scalar(1.0)/(sqrt(\g<var>)"
            for j in range(0, int((i - 1) / 2)):
                repl += r"*\g<var>"
            repl += r"))"
            expr = re.sub(regex, repl, expr)

        # Last-resort for compound-expression pow with single occurrence
        powcount = expr.count("pow")
        commacount = expr.count(",")
        if powcount == 1 and commacount == 1:
            for i in range(0, 7):
                regex = re.compile(rf"pow\((?P<var>\s*([^,]+)),\s*-{i}\.5\s*\)")
                repl = r"(Scalar(1.0)/(sqrt(Scalar(\g<var>))"
                for j in range(1, i + 1):
                    repl += r"*Scalar(\g<var>)"
                repl += r"))"
                expr = re.sub(regex, repl, expr)

        return expr

    # ── fixup: scalar-wrap parameters, apply powsimp ────────────────────

    def fixup(self, expr):
        wrapscalar = False

        for Param, Descr in self.ScalarParams:
            Name = str(Param)
            if Name in expr:
                wrapscalar = True

        for Vec, Name, Descr in self.VectorParams:
            for i, elem in enumerate(Vec):
                symname = str(elem)
                if symname in expr:
                    replname = "({0:}[{1:}])".format(Name, i)
                    regex = re.compile(rf"\b{symname}\b")
                    expr = re.sub(symname, replname, expr)
                    wrapscalar = True

        for Mat, Name, Descr in self.MatrixParams:
            rows = len(Mat)
            cols = len(Mat[0])
            for i in range(0, rows):
                for j in range(0, cols):
                    symname = str(Mat[i, j])
                    if symname in expr:
                        replname = "({0:}({1:},{2:}))".format(Name, i, j)
                        regex = re.compile(rf"\b{symname}\b")
                        expr = re.sub(regex, replname, expr)
                        wrapscalar = True

        # Check for precomputed member references (pcN_)
        for member_name, _ in self._precomputed:
            if member_name in expr:
                wrapscalar = True

        if wrapscalar:
            expr = "Scalar(" + expr + ")"

        expr = self.powsimp(expr)

        return expr

    # ── CSE generation with precomputed hoisting ────────────────────────

    def _gen_cse_body(self, exprs):
        """Run CSE on exprs, partition into param-only and input-dependent.

        Returns (param_cses, input_cses, reduced_exprs) where param_cses
        use member variable names (pcN_) and input_cses are normal CSE.
        """
        cses, reduced = sp.cse(exprs)
        param_cses, input_cses = self._partition_cses(cses)

        if not param_cses:
            return [], input_cses, reduced

        # Build substitution map: param CSE sym → member symbol
        subs_map = {}
        for i, ((cse_sym, _), (member_name, _)) in enumerate(
                zip(param_cses, self._precomputed)):
            subs_map[cse_sym] = sp.Symbol(member_name)

        # Substitute into input-dependent CSE RHS expressions
        new_input_cses = []
        for sym, expr in input_cses:
            for cse_sym, member_sym in subs_map.items():
                expr = expr.subs(cse_sym, member_sym)
            new_input_cses.append((sym, expr))

        # Substitute into reduced expressions
        new_reduced = []
        for item in reduced:
            if isinstance(item, sp.Matrix):
                new_item = item.copy()
                for cse_sym, member_sym in subs_map.items():
                    new_item = new_item.subs(cse_sym, member_sym)
                new_reduced.append(new_item)
            elif isinstance(item, list):
                new_list = []
                for elem in item:
                    for cse_sym, member_sym in subs_map.items():
                        elem = elem.subs(cse_sym, member_sym)
                    new_list.append(elem)
                new_reduced.append(new_list)
            else:
                new_elem = item
                for cse_sym, member_sym in subs_map.items():
                    new_elem = new_elem.subs(cse_sym, member_sym)
                new_reduced.append(new_elem)

        return param_cses, new_input_cses, new_reduced

    # ── class header generation ─────────────────────────────────────────

    def gen_class_header(self):
        I = _IND
        dm = "DenseDerivativeMode::Analytic"

        lines = []
        lines.append(f"struct {self.Name}")
        lines.append(f"{I}: VectorFunction<{self.Name}, {self.ninputs}, "
                      f"{self.noutputs}, {dm}, {dm}> {{")
        lines.append(f"{I}using Base = VectorFunction<{self.Name}, "
                      f"{self.ninputs}, {self.noutputs}, {dm}, {dm}>;")
        lines.append(f"{I}VF_TYPE_ALIASES(Base);")
        lines.append("")

        # Members: scalar params + vector/matrix params
        for P, Descr in self.ScalarParams:
            lines.append(f"{I}double {P}; // {Descr}")
        for Vec, Name, Descr in self.VectorParams:
            vsize = len(Vec)
            lines.append(f"{I}Eigen::Matrix<double, {vsize}, 1> {Name}; // {Descr}")
        for Mat, Name, Descr in self.MatrixParams:
            rows, cols = len(Mat), len(Mat[0])
            lines.append(f"{I}Eigen::Matrix<double, {rows}, {cols}> {Name}; // {Descr}")

        lines.append(f"{I}static constexpr bool is_vectorizable = true;")

        # Precomputed member declarations
        if self._precomputed:
            lines.append("")
            lines.append(f"{I}// Precomputed from constructor parameters")
            for member_name, _ in self._precomputed:
                lines.append(f"{I}double {member_name};")
        lines.append("")

        # Constructor params
        params = []
        for P, Descr in self.ScalarParams:
            params.append(f"double {P}")
        for Vec, Name, Descr in self.VectorParams:
            vsize = len(Vec)
            params.append(f"const Eigen::Matrix<double, {vsize}, 1>& {Name}")
        for Mat, Name, Descr in self.MatrixParams:
            rows, cols = len(Mat), len(Mat[0])
            params.append(f"const Eigen::Matrix<double, {rows}, {cols}>& {Name}")

        paramstr = ", ".join(params)
        assigns = []
        for P, _ in self.ScalarParams:
            assigns.append(f"{P}({P})")
        for Vec, Name, _ in self.VectorParams:
            assigns.append(f"{Name}({Name})")
        for Mat, Name, _ in self.MatrixParams:
            assigns.append(f"{Name}({Name})")
        initlist = ", ".join(assigns)

        # Default constructor
        lines.append(f"{I}{self.Name}() {{")
        lines.append(f"{I}{I}this->set_io_rows({self.ninputs}, {self.noutputs});")
        lines.append(f"{I}}}")

        # Parameterized constructor
        if initlist:
            lines.append(f"{I}{self.Name}({paramstr})")
            lines.append(f"{I}{I}: {initlist} {{")
        else:
            lines.append(f"{I}{self.Name}({paramstr}) {{")
        lines.append(f"{I}{I}this->set_io_rows({self.ninputs}, {self.noutputs});")

        # Precomputed initializations (use std:: prefix for math
        # functions since VF overloads may shadow them in this scope)
        if self._precomputed:
            for member_name, expr in self._precomputed:
                rhs = str(sp.ccode(expr))
                rhs = self.powsimp(rhs)
                # Prefix bare math functions with std::
                for fn in ["sqrt", "sin", "cos", "tan", "asin",
                           "acos", "atan", "atan2", "exp", "log"]:
                    rhs = re.sub(rf"\b{fn}\(", f"std::{fn}(", rhs)
                lines.append(f"{I}{I}{member_name} = {rhs};")

        lines.append(f"{I}}}")
        lines.append("")

        return "\n".join(lines) + "\n"

    # ── compute_impl ────────────────────────────────────────────────────

    def gen_compute_impl(self):
        I = _IND
        lines = []
        lines.append(f"{I}template <class InType, class OutType>")
        lines.append(f"{I}inline void compute_impl(CVecRef<InType> x_,")
        lines.append(f"{I}{I}{I}{I}{I}{I} CVecRef<OutType> fx_) const {{")
        lines.append(f"{I}{I}typedef typename InType::Scalar Scalar;")
        lines.append(f"{I}{I}using std::cos; using std::sin; using std::sqrt;")
        lines.append(f"{I}{I}VecRef<OutType> _fx_ = fx_.const_cast_derived();")
        lines.append("")

        body_lines = self._gen_input_names()
        _, input_cses, funcs = self._gen_cse_body(self.Func)
        body_lines += self._gen_cselist(input_cses)
        body_lines.append("")
        for i, F in enumerate(funcs[0]):
            body_lines.append(self._assignment(f"_fx_[{i}]", sp.ccode(F), False))
        lines += [f"{I}{I}{l}" for l in body_lines]

        lines.append(f"{I}}}")
        lines.append("")
        return "\n".join(lines) + "\n"

    # ── compute_jacobian_impl ───────────────────────────────────────────

    def gen_compute_jacobian_impl(self):
        I = _IND
        lines = []
        lines.append(f"{I}template <class InType, class OutType, class JacType>")
        lines.append(f"{I}inline void compute_jacobian_impl(CVecRef<InType> x_,")
        lines.append(f"{I}{I}{I}{I}{I}{I} CVecRef<OutType> fx_,")
        lines.append(f"{I}{I}{I}{I}{I}{I} CMatRef<JacType> jx_) const {{")
        lines.append(f"{I}{I}typedef typename InType::Scalar Scalar;")
        lines.append(f"{I}{I}using std::cos; using std::sin; using std::sqrt;")
        lines.append(f"{I}{I}VecRef<OutType> _fx_ = fx_.const_cast_derived();")
        lines.append(f"{I}{I}MatRef<JacType> _jx_ = jx_.const_cast_derived();")
        lines.append("")

        body_lines = self._gen_input_names()
        _, input_cses, funcs = self._gen_cse_body([self.Func, self.Jac])
        body_lines += self._gen_cselist(input_cses)
        body_lines.append("")

        for i, F in enumerate(funcs[0]):
            body_lines.append(self._assignment(f"_fx_[{i}]", sp.ccode(F), False))
        body_lines.append("")

        for col in range(self.ninputs):
            for row in range(self.noutputs):
                expr = funcs[1][row, col]
                body_lines.append(self._assignment(
                    f"_jx_({row},{col})", sp.ccode(expr), False))

        lines += [f"{I}{I}{l}" for l in body_lines]
        lines.append(f"{I}}}")
        lines.append("")
        return "\n".join(lines) + "\n"

    # ── compute_jacobian_adjointgradient_adjointhessian_impl ────────────

    def gen_compute_all(self):
        I = _IND
        lines = []
        lines.append(f"{I}template <class InType, class OutType, class JacType,")
        lines.append(f"{I}{I}{I}{I} class AdjGradType, class AdjHessType,")
        lines.append(f"{I}{I}{I}{I} class AdjVarType>")
        lines.append(f"{I}inline void compute_jacobian_adjointgradient_adjointhessian_impl(")
        lines.append(f"{I}{I}CVecRef<InType> x_,")
        lines.append(f"{I}{I}CVecRef<OutType> fx_,")
        lines.append(f"{I}{I}CMatRef<JacType> jx_,")
        lines.append(f"{I}{I}CVecRef<AdjGradType> adjgrad_,")
        lines.append(f"{I}{I}CMatRef<AdjHessType> adjhess_,")
        lines.append(f"{I}{I}CVecRef<AdjVarType> adjvars) const {{")
        lines.append(f"{I}{I}typedef typename InType::Scalar Scalar;")
        lines.append(f"{I}{I}using std::cos; using std::sin; using std::sqrt;")
        lines.append(f"{I}{I}VecRef<OutType> _fx_ = fx_.const_cast_derived();")
        lines.append(f"{I}{I}MatRef<JacType> _jx_ = jx_.const_cast_derived();")
        lines.append(f"{I}{I}VecRef<AdjGradType> _gx_ = adjgrad_.const_cast_derived();")
        lines.append(f"{I}{I}MatRef<AdjHessType> _hx_ = adjhess_.const_cast_derived();")
        lines.append("")

        body_lines = self._gen_input_names()
        body_lines += self._gen_mult_names()
        _, input_cses, funcs = self._gen_cse_body(
            [self.Func, self.Jac, self.Grad, self.Hess])
        body_lines += self._gen_cselist(input_cses)
        body_lines.append("")

        for i, F in enumerate(funcs[0]):
            body_lines.append(self._assignment(f"_fx_[{i}]", sp.ccode(F), False))
        body_lines.append("")
        for col in range(self.ninputs):
            for row in range(self.noutputs):
                body_lines.append(self._assignment(
                    f"_jx_({row},{col})", sp.ccode(funcs[1][row, col]), False))
        body_lines.append("")
        for row in range(self.ninputs):
            body_lines.append(self._assignment(
                f"_gx_[{row}]", sp.ccode(funcs[2][row]), False))

        # Hessian — exploit symmetry: compute upper triangle, copy lower
        body_lines.append("")
        n = self.ninputs
        for col in range(n):
            for row in range(col + 1):  # upper triangle only
                body_lines.append(self._assignment(
                    f"_hx_({row},{col})", sp.ccode(funcs[3][row, col]), False))
            for row in range(col + 1, n):  # lower triangle = copy
                body_lines.append(
                    f"_hx_({row},{col}) = _hx_({col},{row});")

        lines += [f"{I}{I}{l}" for l in body_lines]
        lines.append(f"{I}}}")
        lines.append("")
        return "\n".join(lines) + "\n"

    # ── helpers ─────────────────────────────────────────────────────────

    def _assignment(self, lhs, rhs, is_scalar=False):
        """Return a single assignment line (no leading newline/indent)."""
        rhs = self.fixup(str(rhs))
        prefix = "Scalar " if is_scalar else ""
        return f"{prefix}{lhs} = {rhs};"

    def _gen_input_names(self):
        return [self._assignment(str(X), f"x_[{i}]", True)
                for i, X in enumerate(self.Inputs)]

    def _gen_mult_names(self):
        return [self._assignment(str(L), f"adjvars[{i}]", True)
                for i, L in enumerate(self.LMults)]

    def _gen_cselist(self, cses):
        return [self._assignment(str(cse[0]), sp.ccode(cse[1]), True)
                for cse in cses]

    # ── output ──────────────────────────────────────────────────────────

    def print_header(self):
        print(self._build_text())

    def make_header(self, output_dir=None, script_name=None):
        text = self._build_text(script_name=script_name)
        fname = self.Name + ".h"
        if output_dir:
            fname = os.path.join(output_dir, fname)
        with open(fname, "w") as f:
            f.write(text)

    def _build_text(self, script_name=None):
        copyright = _COPYRIGHT.format(
            script=script_name or "TychoHeaderGen")
        include = f'#pragma once\n#include "{self.include_path}"\n'
        ns_open = f"\nnamespace {self.namespace} {{\n\n{_USING_DECLS}\n"
        header = self.gen_class_header()
        compute = self.gen_compute_impl()
        jacobian = self.gen_compute_jacobian_impl()
        all_impl = self.gen_compute_all()
        ns_close = f"}};\n\n}} // namespace {self.namespace}\n"

        return copyright + include + ns_open + header + compute + jacobian + all_impl + ns_close
