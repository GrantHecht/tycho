"""
Minimum Time-to-Climb — Python Benchmark

Supersonic aircraft minimum-time climb with adaptive mesh refinement.
Aerodynamic tables generated by MinimumTimeToClimbTables.py in examples/.
All plotting code removed. Output suppressed via PrintLevel.
"""

import sys
import time
from pathlib import Path

import numpy as np

# MinimumTimeToClimbTables lives in examples/; add it to path
sys.path.insert(0, str(Path(__file__).parent.parent.parent.parent / "examples"))
from MinimumTimeToClimbTables import (
    CD0Tab,
    ClalphaTab,
    ThrustTab,
    etaTab,
    rhoTab,
    sosTab,
)

import tychopy.OptimalControl as oc
import tychopy.VectorFunctions as vf

g0 = 9.80665
Lstar = 10000
Tstar = 250.0
Mstar = 19050.864

Astar = Lstar / Tstar**2
Vstar = Lstar / Tstar
Rhostar = Mstar / Lstar**3
Fstar = Astar * Mstar

mu = 3.986012e14 / (Lstar**3 / Tstar**2)
Re = 6378145 / Lstar
S = 49.2386 / (Lstar**2)
vexhaust = 1600 * g0 / Vstar


class AirPlane(oc.ODEBase):
    def __init__(self):
        XtU = oc.ODEArguments(4, 1)
        h, v, fpa, mass = XtU.XVec().tolist()
        alpha = XtU.UVar(0)
        rho = rhoTab(h * Lstar) / Rhostar
        sos = sosTab(h * Lstar) / Vstar
        Mach = v / sos
        CD0 = CD0Tab(Mach)
        Clalpha = ClalphaTab(Mach)
        eta = etaTab(Mach)
        Thrust = ThrustTab(Mach, h * Lstar) / Fstar
        CD = CD0 + eta * Clalpha * (alpha**2)
        CL = Clalpha * alpha
        q = 0.5 * rho * (v**2)
        D = q * S * CD
        L = q * S * CL
        r = h + Re
        hdot = v * vf.sin(fpa)
        vdot = (Thrust * vf.cos(alpha) - D) / mass - mu * vf.sin(fpa) / (r**2)
        fpadot = (Thrust * vf.sin(alpha) + L) / (mass * v) + vf.cos(fpa) * (
            v / r - mu / (v * (r**2))
        )
        mdot = -Thrust / vexhaust
        ode = vf.stack([hdot, vdot, fpadot, mdot])
        super().__init__(ode, 4, 1)


if __name__ == "__main__":
    ht0 = 0.010 / Lstar
    htf = 19994.88 / Lstar
    vt0 = 129.314 / Vstar
    vtf = 295.092 / Vstar
    fpat0 = 0
    fpatf = 0
    mass0 = 19050.864 / Mstar

    hmin = 0
    hmax = 21000.0 / Lstar
    vmin = 5 / Vstar
    vmax = 600 / Vstar
    fpamin = -20 * np.pi / 180
    fpamax = 40 * np.pi / 180
    massmin = 16500 / Mstar
    alphamin = -np.pi / 4
    alphamax = np.pi / 4

    tfig = 200 / Tstar

    XtU0 = np.array([ht0, vt0, fpat0, mass0, 0, 0])
    XtUf = np.array([htf, vtf, fpatf, mass0, tfig, 0])
    Traj = [XtU0 * (1 - t) + XtUf * t for t in np.linspace(0, 1, 100)]

    ode = AirPlane()

    phase = ode.phase("LGL5", Traj, 50)
    phase.setControlMode("HighestOrderSpline")
    phase.addBoundaryValue("First", range(0, 5), [ht0, vt0, fpat0, mass0, 0])
    phase.addLUVarBound("Path", 0, hmin, hmax)
    phase.addLUVarBound("Path", 1, vmin, vmax)
    phase.addLUVarBound("Path", 2, fpamin, fpamax)
    phase.addLowerVarBound("Last", 3, massmin)
    phase.addLUVarBound("Path", 5, alphamin, alphamax)
    phase.addBoundaryValue("Last", range(0, 3), [htf, vtf, fpatf])
    phase.addDeltaTimeObjective(1.0)
    phase.setAdaptiveMesh(True)
    phase.optimizer.PrintLevel = 4
    phase.PrintMeshInfo = False
    phase.setNumPartitions(8, 8)
    phase.setMeshTol(1.0e-7)

    start = time.perf_counter()
    phase.optimize()
    end = time.perf_counter()

    print(end - start)
